 public void mostrarDatos(){
   String titulos[] = {"Código", "Asignatura", "Aula", "Estado"};
    String registro[] = new String[4];
    DefaultTableModel modelo = new DefaultTableModel(null, titulos); // Cambia el número de columnas a 4
    String SQL = "SELECT * FROM `horario`";
    try {
        Statement st = (Statement) con.createStatement();
        ResultSet rs = st.executeQuery(SQL);
        while (rs.next()) {
            registro[0] = rs.getString("hora_codigo"); // Coloca el código en la primera columna
            registro[1] = rs.getString("hora_asignatura");
            registro[2] = rs.getString("hora_aula");
            registro[3] = rs.getString("hora_estado");
            modelo.addRow(registro);
        }
        Tablahorario.setModel(modelo);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error al mostrar los datos: " + e);
    }
}
    
    public void limpiarDatos(){
        btnAsignatura.setSelectedItem(null);
        btnAula.setSelectedItem(null);
        btnEstado.setSelectedItem(null);
    }
    
    private void btnGuardar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardar1ActionPerformed
      String asignatura = (String) btnAsignatura.getSelectedItem();
        String aula = (String) btnAula.getSelectedItem();
        String estado = (String) btnEstado.getSelectedItem();
    
        String SQL = "INSERT INTO horario (hora_asignatura, hora_aula, hora_estado) VALUES (?, ?, ?)";

        try {
            PreparedStatement pst = (PreparedStatement) con.prepareStatement(SQL);
            pst.setString(1, asignatura);
            pst.setString(2, aula);
            pst.setString(3, estado);
            pst.execute();

            mostrarDatos();
           
        } catch (Exception e) {
            System.out.println(e);
        }
                               

    }//GEN-LAST:event_btnGuardar1ActionPerformed

