    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
     String SQL = "DELETE FROM horario WHERE hora_codigo = ?";
    
    try {
        int filaSeleccionada = Tablahorario.getSelectedRow();
        if (filaSeleccionada >= 0) {
            String codigo = Tablahorario.getValueAt(filaSeleccionada, 0).toString(); // Obtiene el código de la fila seleccionada
            PreparedStatement pst = (PreparedStatement) con.prepareStatement(SQL);
            pst.setString(1, codigo);
            pst.execute();
            mostrarDatos(); // Actualiza la tabla después de eliminar
            limpiarDatos();
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione un registro para eliminar.");
        }
    } catch (Exception e) {
        System.out.println(e);
    }


    }//GEN-LAST:event_btnEliminarActionPerformed





